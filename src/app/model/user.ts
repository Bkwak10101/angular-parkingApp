export class User {
  name :any
  surname :any
  password :any
  phone: any
  email :any

  constructor(name: any, surname: any, password: any, phone: any, email: any) {
    this.name = name;
    this.surname = surname;
    this.password = password;
    this.phone = phone;
    this.email = email;
  }
}
